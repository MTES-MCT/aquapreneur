			<h1>Conditions générales d’utilisation</h1>
			<p class="fr-text--xs">25 mars 2025</p>

			<h2>Article 1 - Champ d’application</h2>

			<p>
				Le présent document a pour objectif d’encadrer l’utilisation de la
				plateforme Aquapreneur qui est d’accès libre et gratuit.
			</p>
			<p>
				Toute utilisation du Service par un Utilisateur ou Agent Public est
				subordonnée à l’acceptation préalable et au respect intégral des
				présentes conditions générales d’utilisation.
			</p>
			<p>L’usage de la langue française y est obligatoire.</p>

			<h2>Article 2 – Présentation du Service</h2>

			<p>La plateforme Aquapreneur a pour objectifs de :</p>
			<ul>
				<li>
					Permettre aux titulaires d’une concession d’exploitation aquacole de
					télévalider leurs productions pré-saisies si les données sont
					accessibles ou de télédéclarer leurs productions dans les conditions
					prévues par les textes réglementaires ;
				</li>
				<li>
					Mettre à disposition des informations administratives et techniques
					partiellement publiques relatives aux concessions des exploitations
					aquacoles ;
				</li>
				<li>
					À terme de proposer une carte interactive des concessions
					d’exploitation aquacoles ;
				</li>
				<li>
					Accompagner les entreprises face aux aléas et aux pertes de
					production.
				</li>
			</ul>

			<h2>Article 3 - Définitions</h2>

			<p>
				« Administration » : le ministère de la Transition écologique, de la
				Biodiversité, de la Forêt, de la Mer et de la Pêche qui édite et gère le
				Service.
			</p>
			<p>« Agent Public » : personnel de l’Administration.</p>
			<p>
				« Service » : désigne toutes les fonctionnalités offertes par la
				plateforme Aquapreneur pour répondre à ses finalités.
			</p>
			<p>
				« Utilisateur » : personne physique ou morale inscrite sur le Service
				avec un « compte utilisateur » ou un « compte entreprise ».
			</p>
			<p>« Visiteur » : toute personne qui consulte ou utilise le Service.</p>

			<h2>Article 4 - Fonctionnalités</h2>

			<h3>4.1 Visiteur</h3>

			<p>Tout Visiteur peut accéder au Service sans inscription.</p>
			<p>Le Visiteur peut :</p>
			<ul>
				<li>
					Connaître l’état des concessions aquacoles sur le territoire français
					(trois catégories « disponible », « indisponible » et « état
					inconnu ») ;
				</li>
				<li>
					Consulter les informations administratives et techniques publiques
					relatives aux titres aquacoles ;
				</li>
				<li>
					Visualiser une carte et une liste des concessions aquacoles délivrées
					(numéro de la concession, informations techniques de la parcelle, état
					de la parcelle avec les dates des arrêtés ou des enquêtes publiques).
				</li>
			</ul>

			<h3>4.2 Utilisateur</h3>

			<p>
				Pendant sa phase d’expérimentation les fonctionnalités du Service ne
				sont accessibles qu’aux titulaires d’une concession aquacole dans les
				départements de la Charente Maritime, de l’Hérault, de la Manche et du
				Finistère. Le Service est également accessible aux agents publics qui
				créeront une entreprise fictive afin d’expérimenter la plateforme.
			</p>

			<p>En plus des fonctionnalités Visiteur, tout Utilisateur peut :</p>

			<h4>a. Modifier les informations de l’entreprise</h4>

			<p>
				La création d’une entreprise et l’assignation à un utilisateur est
				réalisé par les administrateurs. Ensuite l’utilisateur peut
				renseigner/modifier les informations suivantes : nom de l’entreprise,
				numéro de SIREN, numéro de TVA intracommunautaire, adresse de
				l’entreprise, numéro de téléphone, adresse électronique et nom et
				prénom.
			</p>
			<p>
				L’Utilisateur titulaire du compte peut inviter d’autres Utilisateurs
				pour gérer tout ou partie des données de l’entreprise et de ses
				déclarations.
			</p>
			<p>
				L’Utilisateur peut compléter le profil de son entreprise en ajoutant ses
				employés et dirigeants (nom, prénom, nationalité, fonction, régime de la
				sécurité sociale, diplôme).
			</p>

			<h4>b. Télédéclarer les productions aquacoles d’une entreprise</h4>

			<p>
				L’Utilisateur peut télédéclarer ses productions aquacoles dans les
				conditions prévues par l’article R. 923-11 du code rural et de la pêche
				maritime et son acte de concession.
			</p>
			<p>
				L’Utilisateur peut depuis la page « Votre espace » déterminer le flux
				des notifications afin d’être informé de ses obligations
				télédéclaratives.
			</p>
			<h4>c. Déposer une demande de concession aquacole</h4>
			<p>
				L’Utilisateur peut soumettre une demande de concession aquacole pour
				instruction des services de l’État. Cette fonctionnalité n’est pas
				effective dans la phase d’expérimentation.
			</p>

			<h4>d. Données économiques de l’entreprise</h4>

			<p>
				L’Utilisateur peut renseigner diverses données économiques relatives à
				son entreprise (caractéristiques de l’entreprise, revenus, dépenses,
				capital et investissement, bilan économique et statistiques). Cette
				fonctionnalité n’est pas effective dans la phase d’expérimentation.
			</p>

			<h4>e. Aide</h4>

			<p>
				L’Utilisateur dispose d’une page « Aide » pour l’assister dans
				l’utilisation du Service. Cette fonctionnalité n’est pas effective dans
				la phase d’expérimentation.
			</p>

			<h4>f. Informations réglementaires</h4>

			<p>
				L’Utilisateur dispose d’une page qui liste les informations
				réglementaires sur l’aquaculture en France. Ces informations sont
				renseignées par les administrateurs de la plateforme.
			</p>

			<h3>4.3 Administrations</h3>

			<p>
				Tout Agent Public en charge de l’instruction des concessions et des
				contrôles des exploitations aquacoles peut créer gratuitement un compte
				sur le Service. Dans la phase d’expérimentation, l’Agent Public ne
				pourra administrer l’outil mais simplement se mettre à la place d’un
				exploitant aquacole.
			</p>
			<p>
				Le Service est accessible au moyen d’un identifiant et d’un mot de passe
				librement déterminé par l’Agent Public. Tout Agent Public inscrit peut
				se désinscrire depuis les paramètres de son compte. Celle-ci est
				effective dans un délai deux mois suivant la demande.
			</p>

			<h2>Article 5. Qualité des données publiées</h2>

			<p>
				Les données publiées sur Aquapreneur ont un caractère informatif. Seules
				les publications légales font foi, notamment au Journal officiel de la
				République Française (JORF) ou aux recueils des actes administratifs des
				préfectures (RAA).
			</p>
			<p>
				Les informations publiées sur Aquapreneur ne sauraient engager la
				responsabilité de leur éditeur ou d’un quelconque service de l’État,
				toute personne y ayant recours a conscience des potentielles erreurs ou
				omissions qu’elles peuvent comporter. L’Administration s’efforce de
				livrer des données de la meilleure qualité possible. Toutefois, elles
				peuvent encore comporter des erreurs ou omissions.
			</p>
			<p>
				Toute personne qui constate une erreur ou omission parmi ces données
				peut la signaler à l’aide du bouton « signaler une erreur » disponible
				pour chacune des fiches. Cette fonctionnalité n’est pas disponible dans
				la phase d’expérimentation.
			</p>
			<p>
				Si un titre est manquant, toute personne peut le signaler à
				<a href="mailto:aquapreneur@beta.gouv.fr">aquapreneur@beta.gouv.fr</a>
				en précisant ses coordonnées et en joignant les éventuels documents utiles
				l’identification du titre.
			</p>

			<h2>Article 6 - Responsabilités</h2>

			<h3>6.1 L’Utilisateur</h3>

			<p>
				L’Utilisateur s’assure de garder son mot de passe secret. Toute
				divulgation du mot de passe, quelle que soit sa forme, est interdite. Il
				assume les risques liés à l’utilisation de son identifiant et mot de
				passe.
			</p>
			<p>
				Il s’engage à ne pas commercialiser les données reçues et à ne pas les
				communiquer à des tiers en dehors des cas prévus par la loi. Toute
				information transmise par l’Utilisateur est de sa seule responsabilité.
			</p>
			<p>
				Il est entendu que tous les Utilisateurs d’un même établissement sont
				co-responsables des déclarations et formalités administratives réalisées
				sur le Service.
			</p>
			<p>
				Il est rappelé que toute personne procédant à une fausse déclaration
				pour elle-même ou pour autrui s’expose, notamment, aux sanctions prévues
				à l’article 441-1 du code pénal, prévoyant des peines pouvant aller
				jusqu’à trois ans d’emprisonnement et 45 000 euros d’amende.
			</p>
			<p>
				L’Utilisateur s’engage à ne pas mettre en ligne de contenus ou
				informations contraires aux dispositions légales et réglementaires en
				vigueur.
			</p>
			<p>
				L’Utilisateur s’engage à communiquer des données strictement nécessaires
				à sa demande. Il veille particulièrement aux données sensibles notamment
				les données relatives aux opinions philosophiques, politiques,
				syndicales et religieuses.
			</p>

			<h3>6.2 L’Administration</h3>

			<p>
				L’Administration s’autorise à suspendre ou révoquer n’importe quel
				compte et toutes les actions réalisées par ce biais, s’ils estiment que
				l’usage réalisé du Service porte préjudice à son image ou ne correspond
				pas aux exigences de sécurité.
			</p>
			<p>
				L’Administration s’engage à la sécurisation du Service, notamment en
				prenant toutes les mesures nécessaires permettant de garantir la
				sécurité et la confidentialité des informations fournies.
				L’Administration fournit les moyens nécessaires et raisonnables pour
				assurer un accès continu, sans contrepartie financière, au Service.
			</p>
			<p>
				Elle se réserve la liberté de faire évoluer, de modifier ou de
				suspendre, sans préavis, le Service pour des raisons de maintenance ou
				pour tout autre motif jugé nécessaire.
			</p>

			<h3>6.3 L’Agent Public</h3>

			<p>
				L’Agent public s’assure de garder son mot de passe secret. Toute
				divulgation du mot de passe, quelle que soit sa forme, est interdite. Il
				assume les risques liés à l’utilisation de son identifiant et mot de
				passe.
			</p>
			<p>
				L’Agent Public s’engage à respecter les présentes CGU et leurs
				éventuelles modifications. La méconnaissance par un Agent Public des
				présentes CGU peut donner lieu à des poursuites disciplinaires et devant
				les juridictions compétences.
			</p>

			<h2>Article 7 - Mise à jour des conditions générales d’utilisation</h2>

			<p>
				Les termes des présentes conditions générales d’utilisation peuvent être
				amendés à tout moment, sans préavis, en fonction des modifications
				apportées au Service, de l’évolution de la législation ou pour tout
				autre motif jugé nécessaire.
			</p>

