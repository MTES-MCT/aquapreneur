    <h1>Politique de confidentialité</h1>
    <p class="fr-text--xs">25 mars 2025</p>

    <p>
      Le responsable de traitement s’engage à ce que la collecte et le traitement de vos données
      personnelles, effectués à partir du site soient conformes au règlement européen n° 2016/679
      général sur la protection des données du 27 avril 2016 dit « RGPD » et à la loi n° 78-17
      relative à l’informatique, aux fichiers et aux libertés dite « LIL ».
    </p>

    <p>Aquapreneur est un service numérique qui a pour objectifs de :</p>

    <ul>
      <li>
        Permettre aux titulaires d’une concession d’exploitation aquacole de télévalider leurs
        productions pré-saisies si les données sont accessibles ou de télédéclarer leurs productions
        dans les conditions prévues par les textes réglementaires ;
      </li>
      <li>
        Mettre à disposition des informations administratives et techniques partiellement publiques
        relatives aux concessions des exploitations aquacoles ;
      </li>
      <li>À terme de proposer une carte interactive des concessions d’exploitation aquacoles ;</li>
      <li>Accompagner les entreprises face aux aléas et aux pertes de production.</li>
    </ul>

    <h2>I - Responsable de traitement</h2>
    <p>
      Le responsable de traitement est la Direction générale des affaires maritimes, de la pêche et
      de l’aquaculture (DGAMPA) du ministère de la Transition Écologique et de la Cohésion des
      Territoires, représenté par son directeur général, Eric Banel.
    </p>

    <h2>II - Finalités</h2>

    <p>
      Aquapreneur est un service numérique qui traite des données à caractère personnel pour les
      raisons suivantes :
    </p>
    <ul>
      <li>Créer d’un compte et connexion au Service ;</li>
      <li>Communiquer des données économiques sur une exploitation aquacole ;</li>
      <li>Télédéclarer des productions aquacoles ;</li>
      <li>
        Soumettre une demande de concession aquacole (non disponible dans la phase
        d’expérimentation) ;
      </li>
      <li>
        Mettre en accès libre sur Internet des informations relatives aux exploitations aquacoles
        (non disponible dans la phase d’expérimentation).
      </li>
    </ul>

    <h2>III - Base légale</h2>

    <p>Aquapreneur traite des données à caractère personnel en se basant sur :</p>
    <ul>
      <li>
        L’exécution d’une mission d’intérêt public ou relevant de l’exercice de l’autorité publique
        dont est investi le responsable de traitement au sens de l’article 6-1 e) du RPGD ;
      </li>
      <li>
        Cette mission d’intérêt public découle des attributions confiées à la Direction générale des
        affaires maritimes, de la pêche et de l’aquaculture (DGAMPA) par l’article 9 du décret n°
        2008-680, l’article 9.1.1 de l’arrêté du 9 juillet 2008 portant organisation des
        administrations centrales des ministères chargés de la transition écologique, de la cohésion
        des territoires et de la mer et l’article R. 923-11 du code rural et de la pêche maritime.
      </li>
    </ul>

    <h2>IV - Catégories de données</h2>

    <p>Aquapreneur traite les données à caractère personnel suivantes :</p>

    <ul>
      <li>
        <strong>Données de création de compte et de connexion</strong> : Nom, prénom, adresse mail
      </li>
      <li>
        <strong>Données d’entreprise</strong> : nom, prénom, téléphone, courrier électronique ainsi
        que le nom de l’entreprise, numéros de SIREN, numéro de TVA intracommunautaire et l’adresse
        du siège social.
      </li>
      <li>
        <strong>Données relatives aux demandes d’une concession aquacole</strong> : Non disponible
        dans la phase d’expérimentation.
      </li>
      <li>
        <strong>Données des concessions d’exploitation aquacole en accès libre</strong> : toutes les
        informations nécessaires à l’établissement et la consultation d’une cartographie interactive
        qu’il s’agisse ou non de données à caractère personnel et notamment, géolocalisation de la
        parcelle cadastrale. Le nom de l’entreprise et l’identité du représentant légal ne sont pas
        disponibles dans la phase d’expérimentation.
      </li>
    </ul>

