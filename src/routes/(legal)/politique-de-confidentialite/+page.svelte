    <h2>X - Cookies</h2>

    <p>
      Un cookie est un fichier déposé sur votre terminal lors de la visite d’un site. Il a pour but
      de collecter des informations relatives à votre navigation et de vous adresser des services
      adaptés à votre terminal (ordinateur, mobile ou tablette).
    </p>

    <p>
      En application de l’article 5(3) de la directive 2002/58/CE modifiée concernant le traitement
      des données à caractère personnel et la protection de la vie privée dans le secteur des
      communications électroniques, transposée à l’article 82 de la loi n° 78-17 du 6 janvier 1978
      relative à l’informatique, aux fichiers et aux libertés, les traceurs ou cookies suivent deux
      régimes distincts.
    </p>

    <ul>
      <li>
        Les cookies strictement nécessaires au service ou ayant pour finalité exclusive de faciliter
        la communication par voie électronique sont dispensés de consentement préalable au titre de
        l’article 82 de la loi n° 78-17 du 6 janvier 1978.
      </li>
      <li>
        Les cookies n’étant pas strictement nécessaires au service ou n’ayant pas pour finalité
        exclusive de faciliter la communication par voie électronique doivent être consenti par
        l’utilisateur.
      </li>
    </ul>
    <p>
      Ce consentement de la personne concernée pour une ou plusieurs finalités spécifiques constitue
      une base légale au sens du RGPD et doit être entendu au sens de l’article 6-1 a) du RGPD et à
      la libre circulation des données.
    </p>

    <p>
      À tout moment, vous pouvez refuser l’utilisation des cookies et désactiver le dépôt sur votre
      ordinateur en utilisant la fonction dédiée de votre navigateur (fonction disponible notamment
      sur Microsoft Edge, Google Chrome, Mozilla Firefox, Apple Safari et Opera).
    </p>
